"""Greeting service — personalized arrival greetings via presence events.

Listens for ``presence.arrived`` events and announces a unique greeting
over speakers using TTS. Tracks who has been greeted today to avoid
repeats. Greeting text is generated by the AI service.
"""

from __future__ import annotations

import asyncio
import logging
from datetime import UTC, datetime
from typing import Any

from gilbert.interfaces.ai import AIProvider, AISamplingProvider, Message, MessageRole
from gilbert.interfaces.auth import UserContext
from gilbert.interfaces.configuration import ConfigParam
from gilbert.interfaces.events import Event, EventBus, EventBusProvider
from gilbert.interfaces.feeds import FeedsProvider
from gilbert.interfaces.presence import PresenceProvider
from gilbert.interfaces.service import Service, ServiceInfo, ServiceResolver
from gilbert.interfaces.speaker import SpeakerProvider
from gilbert.interfaces.storage import StorageProvider
from gilbert.interfaces.tools import (
    ToolDefinition,
    ToolParameter,
    ToolParameterType,
)
from gilbert.interfaces.users import UserManagementProvider
from gilbert.interfaces.weather import (
    LocationNotConfiguredError,
    WeatherProvider,
    WeatherUnavailableError,
    WeatherUnits,
)

logger = logging.getLogger(__name__)

_GREETING_COLLECTION = "greeting_state"

# Manual ``/greet`` only uses the resolved user_id when the match
# confidence clears this bar. Below the threshold we still greet by
# the raw name, but don't pull recent-greeting history or mark anyone
# as greeted — those are user-keyed records and we'd rather drop them
# than attribute them to the wrong person.
_GREETING_MATCH_CONFIDENCE_THRESHOLD: float = 0.7

# Default template for the manual ``/greet`` AI prompt. The live value
# is read from ``greeting.enhanced_greeting_prompt`` and cached on
# ``self._enhanced_greeting_prompt`` so the Settings UI can tune it
# without a restart. Placeholders are filled with ``.format_map`` —
# missing ones in a user-edited version render empty, malformed
# templates fall back to this default at call time.
_DEFAULT_ENHANCED_GREETING_PROMPT = """\
Generate a personalized morning greeting for {name}. You're Gilbert, an AI \
assistant at a business — feel free to use any read-only data-lookup tools \
available to you (presence, recent activity, project status, time logs, etc.) \
to gather context that could make the greeting feel personal. Vary your tone \
across days (witty, warm, dramatic, deadpan, poetic, nerdy, etc.). Mention \
their name. 1-2 sentences.

Do NOT call any tool that broadcasts, announces, sends a message, or plays \
audio — the greeting is delivered separately by the calling code. Reply with \
ONLY the greeting text — no quotes, no preamble, no commentary on which tools \
you used.{style_instruction}{avoid_section}"""

_DEFAULT_WEATHER_HINT_TEMPLATE = (
    "Current weather at {location_name}: {temperature:.0f}{temp_suffix} "
    "{condition_phrase}, wind {wind_speed:.0f}{speed_suffix}"
    "{feels_like_clause}. Mention it casually if it fits the moment, "
    "otherwise ignore. Quote only the values shown — never invent additional "
    "weather details."
)


class GreetingService(Service):
    """Personalized arrival greetings driven by presence events.

    Capabilities: greeting
    """

    def __init__(self) -> None:
        self._enabled: bool = False
        self._event_bus: EventBus | None = None
        self._storage_backend: Any = None  # StorageBackend
        self._ai: Any = None  # AIService
        self._speaker: Any = None  # SpeakerService
        self._resolver: ServiceResolver | None = None
        self._unsubscribe: Any = None
        # Per-user lock prevents concurrent presence events from
        # double-greeting the same person (TOCTOU on _has_been_greeted).
        self._greeting_locks: dict[str, asyncio.Lock] = {}
        self._greeting_locks_guard = asyncio.Lock()

        # Config
        self._start_hour: int = 6
        self._cutoff_hour: int = 14
        self._style: str = ""
        self._speakers: list[str] = []
        self._timezone: str = "UTC"
        self._ai_profile: str = "light"
        self._enhanced_greeting_prompt: str = _DEFAULT_ENHANCED_GREETING_PROMPT
        self._include_weather: bool = True
        self._weather_hint_template: str = _DEFAULT_WEATHER_HINT_TEMPLATE
        self._weather: WeatherProvider | None = None
        self._include_briefing: bool = False
        self._briefing_max_seconds: int = 60
        self._feeds: FeedsProvider | None = None

    def service_info(self) -> ServiceInfo:
        return ServiceInfo(
            name="greeting",
            capabilities=frozenset({"greeting", "ai_tools"}),
            requires=frozenset({"event_bus", "entity_storage"}),
            optional=frozenset(
                {
                    "ai",
                    "speaker_control",
                    "text_to_speech",
                    "presence",
                    "configuration",
                    "feeds",
                }
            ),
            ai_calls=frozenset({"greeting"}),
            events=frozenset({"greeting.announced"}),
            toggleable=True,
            toggle_description="Automated greeting announcements",
        )

    async def start(self, resolver: ServiceResolver) -> None:
        self._resolver = resolver

        # Check enabled
        config_svc = resolver.get_capability("configuration")
        if config_svc is not None:
            from gilbert.interfaces.configuration import ConfigurationReader

            if isinstance(config_svc, ConfigurationReader):
                section = config_svc.get_section(self.config_namespace)
                if not section.get("enabled", False):
                    logger.info("Greeting service disabled")
                    return

        self._enabled = True

        # Get event bus and subscribe
        event_bus_svc = resolver.require_capability("event_bus")
        if not isinstance(event_bus_svc, EventBusProvider):
            raise RuntimeError("event_bus capability does not satisfy EventBusProvider")
        self._event_bus = event_bus_svc.bus
        self._unsubscribe = self._event_bus.subscribe("presence.arrived", self._on_arrival)

        # Entity storage for dedup tracking
        storage_svc = resolver.require_capability("entity_storage")
        if isinstance(storage_svc, StorageProvider):
            self._storage_backend = storage_svc.backend

        # Load config
        if config_svc is not None:
            from gilbert.interfaces.configuration import ConfigurationReader

            if isinstance(config_svc, ConfigurationReader):
                section = config_svc.get_section(self.config_namespace)
                self._start_hour = int(section.get("start_hour", 6))
                self._cutoff_hour = int(section.get("cutoff_hour", 14))
                self._style = section.get("style", "")
                self._speakers = section.get("speakers", [])
                self._timezone = section.get("timezone", "UTC")
                self._enhanced_greeting_prompt = (
                    section.get("enhanced_greeting_prompt") or _DEFAULT_ENHANCED_GREETING_PROMPT
                )
                self._include_weather = bool(
                    section.get("include_weather", self._include_weather)
                )
                self._weather_hint_template = section.get(
                    "weather_hint_template",
                    self._weather_hint_template,
                ) or _DEFAULT_WEATHER_HINT_TEMPLATE
                self._include_briefing = bool(
                    section.get("include_briefing", self._include_briefing)
                )
                self._briefing_max_seconds = int(
                    section.get("briefing_max_seconds", self._briefing_max_seconds)
                    or self._briefing_max_seconds
                )

        # Optional weather provider — if missing or wrong protocol,
        # degrade to the no-weather greeting.
        weather_svc = resolver.get_capability("weather")
        if isinstance(weather_svc, WeatherProvider):
            self._weather = weather_svc

        feeds_svc = resolver.get_capability("feeds")
        if isinstance(feeds_svc, FeedsProvider):
            self._feeds = feeds_svc

        # Schedule a one-shot check for people already present at startup.
        # This handles the case where Gilbert restarts while people are
        # already at the shop — they won't get a presence.arrived event
        # because the presence service suppresses events for users seen
        # on the first poll.
        scheduler = resolver.get_capability("scheduler")
        if scheduler is not None:
            from gilbert.interfaces.scheduler import Schedule, SchedulerProvider

            if isinstance(scheduler, SchedulerProvider):
                scheduler.add_job(
                    name="greeting-startup-check",
                    schedule=Schedule.once_after(45),
                    callback=self._greet_already_present,
                    system=True,
                )

        logger.info(
            "Greeting service started (window=%d:00-%d:00)",
            self._start_hour,
            self._cutoff_hour,
        )

    async def stop(self) -> None:
        if self._unsubscribe:
            self._unsubscribe()

