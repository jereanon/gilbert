    async def start(self, resolver: ServiceResolver) -> None:
        self._resolver = resolver
        self._discover_context_providers()

        # Check enabled
        config_svc = resolver.get_capability("configuration")
        if config_svc is not None:
            from gilbert.interfaces.configuration import ConfigurationReader

            if isinstance(config_svc, ConfigurationReader):
                section = config_svc.get_section(self.config_namespace)
                if not section.get("enabled", False):
                    logger.info("Greeting service disabled")
                    return

        self._enabled = True

        # Get event bus and subscribe
        event_bus_svc = resolver.require_capability("event_bus")
        if not isinstance(event_bus_svc, EventBusProvider):
            raise RuntimeError("event_bus capability does not satisfy EventBusProvider")
        self._event_bus = event_bus_svc.bus
        self._unsubscribe = self._event_bus.subscribe("presence.arrived", self._on_arrival)
        # Camera-detected events fan-out for the announce path; per-label
        # config gates whether we actually announce.
        self._unsubscribe_camera = self._event_bus.subscribe(
            "camera.event.detected", self._on_camera_event
        )

        # Entity storage for dedup tracking
        storage_svc = resolver.require_capability("entity_storage")
        if isinstance(storage_svc, StorageProvider):
            self._storage_backend = storage_svc.backend

        # Load config
        if config_svc is not None:
            from gilbert.interfaces.configuration import ConfigurationReader

            if isinstance(config_svc, ConfigurationReader):
                section = config_svc.get_section(self.config_namespace)
                self._start_hour = int(section.get("start_hour", 6))
                self._cutoff_hour = int(section.get("cutoff_hour", 14))
                self._style = section.get("style", "")
                self._speakers = section.get("speakers", [])
                self._timezone = section.get("timezone", "UTC")
                self._enhanced_greeting_prompt = (
                    section.get("enhanced_greeting_prompt") or _DEFAULT_ENHANCED_GREETING_PROMPT
                )
                self._arrival_greeting_prompt = (
                    section.get("arrival_greeting_prompt") or _DEFAULT_ARRIVAL_GREETING_PROMPT
                )

        # Schedule a one-shot check for people already present at startup.
        # This handles the case where Gilbert restarts while people are
        # already at the shop — they won't get a presence.arrived event
        # because the presence service suppresses events for users seen
        # on the first poll.
        scheduler = resolver.get_capability("scheduler")
        if scheduler is not None:
            from gilbert.interfaces.scheduler import Schedule, SchedulerProvider

            if isinstance(scheduler, SchedulerProvider):
                scheduler.add_job(
                    name="greeting-startup-check",
                    schedule=Schedule.once_after(45),
                    callback=self._greet_already_present,
                    system=True,
                )

        logger.info(
            "Greeting service started (window=%d:00-%d:00)",
            self._start_hour,
            self._cutoff_hour,
        )

    async def stop(self) -> None:
        if self._unsubscribe:
            self._unsubscribe()
        if self._unsubscribe_camera:
            self._unsubscribe_camera()
            self._unsubscribe_camera = None

