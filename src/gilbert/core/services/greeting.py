    async def on_config_changed(self, config: dict[str, Any]) -> None:
        self._start_hour = int(config.get("start_hour", self._start_hour))
        self._cutoff_hour = int(config.get("cutoff_hour", self._cutoff_hour))
        self._style = config.get("style", self._style)
        self._ai_profile = config.get("ai_profile", self._ai_profile)
        self._speakers = config.get("speakers", self._speakers)
        self._timezone = config.get("timezone", self._timezone)
        if "arrival_greeting_prompt" in config:
            self._arrival_greeting_prompt = (
                config["arrival_greeting_prompt"] or _DEFAULT_ARRIVAL_GREETING_PROMPT
            )
        if "enhanced_greeting_prompt" in config:
            # Empty string explicitly clears back to the bundled default.
            self._enhanced_greeting_prompt = (
                config["enhanced_greeting_prompt"] or _DEFAULT_ENHANCED_GREETING_PROMPT
            )
        labels = config.get("announce_camera_labels")
        if isinstance(labels, list):
            self._announce_camera_labels = tuple(str(label) for label in labels)
        try:
            self._camera_announce_dedup_seconds = float(
                config.get(
                    "camera_announce_dedup_seconds",
                    self._camera_announce_dedup_seconds,
                )
            )
        except (TypeError, ValueError):
            self._camera_announce_dedup_seconds = (
                _DEFAULT_CAMERA_ANNOUNCE_DEDUP_SECONDS
            )
        keys = config.get("camera_announce_dedup_keys")
        if isinstance(keys, dict):
            self._camera_announce_dedup_keys = {
                str(k): [str(part) for part in v if isinstance(part, str)]
                for k, v in keys.items()
                if isinstance(v, list)
            }
        zone_groups = config.get("camera_zone_groups")
        if isinstance(zone_groups, dict):
            self._camera_zone_groups = {
                str(g): [str(c) for c in cams if isinstance(c, str)]
                for g, cams in zone_groups.items()
                if isinstance(cams, list)
            }
            inverse: dict[str, str] = {}
            for group, cams in self._camera_zone_groups.items():
                for cam in cams:
                    inverse[cam] = group
            self._camera_to_zone_group = inverse
        prompt = config.get("camera_announce_prompt")
        if isinstance(prompt, str) and prompt.strip():
            self._camera_announce_prompt = prompt
        else:
            self._camera_announce_prompt = _DEFAULT_CAMERA_ANNOUNCE_PROMPT
        per_label = config.get("camera_announce_per_label_prompts")
        if isinstance(per_label, dict):
            self._camera_announce_per_label_prompts = {
                str(k): str(v)
                for k, v in per_label.items()
                if isinstance(v, str) and v.strip()
            }
        if "enabled_context_providers" in config:
            raw = config["enabled_context_providers"]
            self._enabled_context_providers = list(raw) if raw is not None else None

    async def _greet_already_present(self) -> None:
        """Greet anyone already present at startup who hasn't been greeted today.

        Instead of greeting each person individually (which would cause
        announcements to step on each other), generates a single combined
        greeting for everyone present.

        Marks users as greeted BEFORE announcing so any concurrent
        ``presence.arrived`` event (which can fire seconds after the
        startup poll) sees them as already greeted and doesn't pile on.
        """
        if not self._in_greeting_window():
            logger.info("Startup greeting skipped — outside greeting window")
            return

        presence_svc = self._resolver.get_capability("presence") if self._resolver else None
        if not isinstance(presence_svc, PresenceProvider):
            logger.info("Startup greeting skipped — no presence service")
            return

        try:
            here = await presence_svc.who_is_here()
        except Exception:
            logger.warning("Startup greeting: could not check who is here", exc_info=True)
            return

        # Filter to people not yet greeted today
        to_greet_names: list[str] = []
        to_greet_ids: list[str] = []
        for p in here:
            if not await self._has_been_greeted_today(p.user_id):
                name = await self._get_display_name(p.user_id)
                to_greet_names.append(name)
                to_greet_ids.append(p.user_id)

        logger.info(
            "Startup greeting: %d present, %d to greet",
            len(here),
            len(to_greet_names),
        )

        if not to_greet_names:
            return

        # Mark as greeted FIRST — before AI generation and announce —
        # so concurrent _on_arrival handlers see the flag immediately.
        for uid in to_greet_ids:
            await self._mark_greeted(uid, "")

        # Collect recent greetings across all users for variety
        all_recent: list[str] = []
        for p in here:
            all_recent.extend(await self._get_recent_greetings(p.user_id))

        # Generate a single combined greeting.
        # Group greetings pass user_ids so single-person groups still
        # get per-user context; true multi-person groups skip per-user
        # context for simplicity in v1.
        greeting = await self._generate_group_greeting(
            to_greet_names, all_recent, user_ids=to_greet_ids
        )
        logger.info("Startup greeting: %s", greeting)

        await self._announce(greeting)

        # Update the greeting text now that we have it
        for uid in to_greet_ids:
            await self._mark_greeted(uid, greeting)

        if self._event_bus:
            await self._event_bus.publish(
                Event(
                    event_type="greeting.announced",
                    data={
                        "names": to_greet_names,
                        "greeting": greeting,
                        "startup": True,
                    },
                    source="greeting",
                )
            )

    async def _on_arrival(self, event: Event) -> None:
        """Handle a presence.arrived event."""
        user_id = event.data.get("user_id", "")
        if not user_id:
            return
        if not self._in_greeting_window():
            return
        await self._greet_user(user_id)

    async def _greet_user(self, user_id: str) -> None:
        """Greet a user if they haven't been greeted today.

        Serialized per-user so concurrent ``presence.arrived`` events
        for the same person can't race past the greeted-today check.
        """
        # Get or create per-user lock
        async with self._greeting_locks_guard:
            if user_id not in self._greeting_locks:
                self._greeting_locks[user_id] = asyncio.Lock()
            lock = self._greeting_locks[user_id]

        async with lock:
            if await self._has_been_greeted_today(user_id):
                return

            display_name = await self._get_display_name(user_id)
            recent = await self._get_recent_greetings(user_id)
            greeting = await self._generate_greeting(
                display_name, recent, user_id=user_id
            )

            logger.info("Greeting %s: %s", user_id, greeting)

            await self._mark_greeted(user_id, greeting)
            await self._announce(greeting)

            if self._event_bus:
                await self._event_bus.publish(
                    Event(
                        event_type="greeting.announced",
                        data={
                            "user_id": user_id,
                            "greeting": greeting,
                        },
                        source="greeting",
                    )
                )

    def _in_greeting_window(self) -> bool:
        """Check if current time is within the greeting window."""
        try:
            from zoneinfo import ZoneInfo

            now = datetime.now(ZoneInfo(self._timezone))
        except Exception:
            now = datetime.now(UTC)

        return self._start_hour <= now.hour < self._cutoff_hour

    async def _get_display_name(self, user_id: str) -> str:
        """Resolve a user_id to a display-friendly first name."""
        from gilbert.core.user_utils import resolve_display_name

        return await resolve_display_name(user_id, self._resolver, first_name_only=True)

    async def _has_been_greeted_today(self, user_id: str) -> bool:
        """Check if user has been greeted today."""
        if self._storage_backend is None:
            return False

        record = await self._storage_backend.get(_GREETING_COLLECTION, user_id)
        if not record:
            return False

        today = self._today_str()
        return bool(record.get("last_greeting_date") == today)

    async def _mark_greeted(self, user_id: str, greeting: str = "") -> None:
        """Mark user as greeted today and store the greeting text."""
        if self._storage_backend is None:
            return

        today = self._today_str()
        record = await self._storage_backend.get(_GREETING_COLLECTION, user_id)
        count = (record.get("greeting_count", 0) if record else 0) + 1

        # Keep a rolling log of recent greetings so the AI can avoid repeats
        recent: list[str] = record.get("recent_greetings", []) if record else []
        if greeting:
            recent.append(greeting)
            recent = recent[-10:]  # keep last 10

        await self._storage_backend.put(
            _GREETING_COLLECTION,
            user_id,
            {
                "user_id": user_id,
                "last_greeting_date": today,
                "greeting_count": count,
                "recent_greetings": recent,
            },
        )

    async def _get_recent_greetings(self, user_id: str) -> list[str]:
        """Get recent greeting texts for a user (for anti-repetition)."""
        if self._storage_backend is None:
            return []
        record = await self._storage_backend.get(_GREETING_COLLECTION, user_id)
        if not record:
            return []
        recent = record.get("recent_greetings", [])
        return list(recent) if isinstance(recent, list) else []

    def _today_str(self) -> str:
        """Get today's date string in the configured timezone."""
        try:
            from zoneinfo import ZoneInfo

            return datetime.now(ZoneInfo(self._timezone)).strftime("%Y-%m-%d")
        except Exception:
            return datetime.now(UTC).strftime("%Y-%m-%d")

    def _discover_context_providers(self) -> None:
        """Populate self._context_providers from the resolver.

        Idempotent: callable any time after self._resolver is set.
        """
        if self._resolver is None:
            self._context_providers = []
            return
        self._context_providers = [
            svc for svc in self._resolver.get_all("greeting_context")
            if isinstance(svc, GreetingContextProvider)
        ]

